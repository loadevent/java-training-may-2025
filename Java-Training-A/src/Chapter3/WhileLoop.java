package Chapter3;
/*Use a While Loop when you don't know the
number of iterations
 */
public class WhileLoop {
    public static void main(String[] args) {
        //Display Wednesday 5 time;
        //declare and initialize the loop control variable
        int count = 1;
        //test the loop control variable
        while(count == 5){
            System.out.println("Wednesday");//1
            //change the value of loop control variable
            count++;
        }
    }
}
